class Pixel < Formula
  desc "Pageking CLI for initializing projects"
  homepage "https://github.com/Pageking/pixel"
  url "https://github.com/Pageking/pixel/archive/refs/tags/v1.4.6.tar.gz"
  sha256 "153119a362c40714729ad7a6f49bc1f6f9570255d0ab4f9fbfe2b217d78e37dc"
