class Pixel < Formula
  desc "Pageking CLI for initializing projects"
  homepage "https://github.com/Pageking/pixel"
  url "https://github.com/Pageking/pixel/archive/refs/tags/v1.4.9.tar.gz"
  sha256 "e5ac6d416a425a4ca301f3f23d14d19bebfba66602171475cca571bf4a0cebb1"
